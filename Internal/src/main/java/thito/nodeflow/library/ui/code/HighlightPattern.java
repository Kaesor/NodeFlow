package thito.nodeflow.library.ui.code;

public class HighlightPattern {
    private String name;
    private String pattern;

    public HighlightPattern(String name, String pattern) {
        this.name = name;
        this.pattern = pattern;
    }

    public String getName() {
        return name;
    }

    public String getPattern() {
        return pattern;
    }

}
