package thito.nodeflow.library.ui.layout;

public class LayoutParserException extends Exception {
    public LayoutParserException() {
    }

    public LayoutParserException(String message) {
        super(message);
    }

    public LayoutParserException(String message, Throwable cause) {
        super(message, cause);
    }

    public LayoutParserException(Throwable cause) {
        super(cause);
    }
}
